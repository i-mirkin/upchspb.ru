 <b><i>Журналистская и преподавательская деятельность</i></b>
<p>
	 В 1989 году стала учредителем информационно-рекламного агентства ИМА-ПРЕСС, работала редактором и ведущей программы ИМА-ПРЕСС-радио, ведущей телепередачи «Завтрак бизнесмена».
</p>
<p>
	 С 1991 года вернулась <b>на телевидение</b> в ГТРК <b>"Петербург - 5-й канал"</b>. Работала редактором редакции Рекламы, корреспондентом, редактором, комментатором <b>"Информ-ТВ"</b>, автором и ведущей программ <b>"У всех на виду"</b> и <b>"Пять вечеров"</b>.
</p>
<p>
	 С 2000 года на <b>телеканале "</b><b>Россия</b><b>-Санкт-Петербург"</b> вела авторские программы <b>"Погода в доме"</b> и <b>"Детский вопрос"</b>.
</p>
<p>
	 Член Союза журналистов Санкт-Петербурга и Ленинградской области с 1990 года.
</p>
<p>
	 Работу на телевидении <b>совмещала с преподавательской деятельностью</b> на факультете журналистики Санкт-Петербургского Государственного Университета – вела спецкурсы и спецсеминары.
</p>
<p>
	 В октябре 2000 года защитила <b>кандидатскую диссертацию</b> по теме: «Информационное вещание Ленинградского – Санкт-Петербургского телевидения: опыт и перспективы развития (1938-1998 гг.)», стала доцентом кафедры Радио и телевидения.
</p>
