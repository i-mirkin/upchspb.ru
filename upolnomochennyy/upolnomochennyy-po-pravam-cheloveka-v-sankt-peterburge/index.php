<p>
	 В 1986 году с отличием закончила факультет журналистики Ленинградского Государственного Университета им. А. А. Жданова. Во время учебы была внештатным корреспондентом спортивной редакции Ленинградского телевидения. По распределению пошла работать в отдел пропаганды Ленинградского горкома ВЛКСМ.
