<p>
 <b><i>Правозащитная деятельность </i></b>
</p>
<p>
	 23 декабря 2009 года депутатами Законодательного собрания <b>избрана первым Уполномоченным по правам ребенка в Санкт-Петербурге</b>.
</p>
<p>
	 21 января 2015 года <b>избрана на эту должность на второй пятилетний срок</b>.
</p>
<p>
	 Поскольку раньше в нашем городе не было государственного института защиты детей, первый Уполномоченный формировала его «с нуля»: создавала аппарат, сайт, приемную.
</p>
<p>
	 Разрабатывала <b>основные принципы работы</b>: гласность, открытость, стремление помочь каждому нуждающемуся, необходимость добиваться справедливости, примирения взрослых ради детей, умение слушать и слышать людей, систематизировать жалобы и добиваться изменения законодательства в интересах детей и семей.
</p>
<p>
	 В 2012 году закончила <b>юридический факультет Российской Академии народного хозяйства и государственной службы при Президенте РФ</b>.
</p>
<p>
	 В 2011 году избрана Председателем Координационного совета Уполномоченных по правам ребенка в Северо-Западном Федеральном округе. Возглавляла совет до окончания срока полномочий.
</p>
<p>
	 На протяжении десяти лет вместе с сотрудниками аппарата Уполномоченного по правам ребенка велась длительная и кропотливая системная работа в тесном взаимодействии с органами исполнительной и законодательной власти, общественными организациями. Для того, чтобы сделать жизнь маленьких петербуржцев комфортнее и благополучнее <b>внесен ряд важных изменений в федеральное и городское законодательство</b>.
</p>
<p>
	 В должности Уполномоченного по правам ребенка работала в коллегиальных органах федерального уровня:
