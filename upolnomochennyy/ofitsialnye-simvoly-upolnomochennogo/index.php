<h2 style="clear: both; text-align: justify;">
Должностная эмблема Уполномоченного<br>
 </h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <a href="/upolnomochennyy/ofitsialnye-simvoly-upolnomochennogo/sppc-C-embl.jpg" target="_blank">Должностная эмблема Уполномоченного</a>&nbsp;– опознавательно-правовой и представительский знак, являющийся вспомогательным символом Уполномоченного; эмблема воспроизводит герб Уполномоченного в круглом щите, дополненном наименованием должности.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <img alt="Эмблема уполномоченного по правам человека" src="/upolnomochennyy/ofitsialnye-simvoly-upolnomochennogo/	PECHAT_UPCH.png" title="Эмблема" style="margin: 0 15px 15px 0; float:left; width:200px; height: auto;" align="left">Эмблема Уполномоченного основана на его гербе и представляет последний в круге (круглом щите), окаймленном тонким серебряным кольцом; все вместе помещено внутри такого же кольца большего диаметра; в широком лазоревом кольце, образованном серебряными кольцами, – две надписи серебряными литерами: вверху – УПОЛНОМОЧЕННЫЙ ПО ПРАВАМ ЧЕЛОВЕКА, внизу – САНКТ-ПЕТЕРБУРГ; надписи разделены срединными точками в цвет литер. Вся эмблема окаймлена по периметру тонким лазоревым кольцом.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Эмблема Уполномоченного может заместить герб во всех случаях, когда есть необходимость сопроводить официальный символ Уполномоченного текстовым указанием (наименованием должности).
</p>
