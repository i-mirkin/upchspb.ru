<h2>
Право поступления на гражданскую службу </h2>
<p>
	 На гражданскую службу вправе поступать граждане Российской Федерации, достигшие возраста 18 лет, владеющие государственным языком Российской Федерации и соответствующие квалификационным требованиям, установленным законодательством.
</p>
<h2>
Поступление гражданина на гражданскую службу </h2>
<p>
	 Поступление гражданина на гражданскую службу в аппарат Уполномоченного для замещения должности гражданской службы или замещение гражданским служащим другой должности гражданской службы в аппарате Уполномоченного осуществляется по результатам конкурса, за исключением случаев, предусмотренных законодательством о государственной гражданской службе.
</p>
<p>
	 Конкурс заключается в оценке профессионального уровня претендентов на замещение должности гражданской службы, их соответствия установленным квалификационным требованиям для замещения должности гражданской службы.
</p>
<p>
	 При объявлении конкурса на замещение вакантных должностей государственной гражданской службы в аппарат Уполномоченного информация об этом публикуется на официальном&nbsp;сайте&nbsp;государственного органа Уполномоченный по правам человека в Санкт-Петербурге (<a href="https://upchspb.ru/">https://upchspb.ru/</a>)
</p>
<h2>
Квалификационные требования при поступлении на гражданскую службу в аппарат Уполномоченного </h2>
<p>
	 Для замещения должности государственной гражданской службы в аппарате Уполномоченного требуется соответствие квалификационным требованиям к уровню профессионального образования, стажу государственной гражданской службы или работы по специальности, направлению подготовки, знаниям и умениям, которые необходимы для исполнения должностных обязанностей, а также при наличии соответствующего решения представителя нанимателя – к специальности, направлению подготовки.
</p>
<p>
	 Квалификационные требования для замещения должностей гражданской службы устанавливаются в соответствии с категориями и группами должностей гражданской службы, областью и видом профессиональной служебной деятельности гражданского служащего.
</p>
<h3>
Требования к уровню профессионального образования: </h3>
<p>
	 Для замещения должностей гражданской службы категорий «руководители», «помощники (советники)», «специалисты» высшей группы должностей гражданской службы обязательно наличие высшего образования не ниже уровня специалитета, магистратуры.
</p>
<p>
	 Для замещения должностей гражданской службы категорий «специалисты» главной группы должностей гражданской службы обязательно наличие высшего образования не ниже уровня специалитета, магистратуры.
</p>
<p>
	 Для замещения должностей гражданской службы категорий «специалисты», «обеспечивающие специалисты» ведущей группы должностей гражданской службы обязательно наличие высшего образования.
</p>
<p>
	 Для замещения должностей гражданской службы категории «обеспечивающие специалисты» старшей группы должностей гражданской службы обязательно наличие профессионального образования.
</p>
<h3>
Требования к стажу государственной гражданской службы или стажу работы по специальности, направлению подготовки: </h3>
<p>
</p>
<ol>
	<li>
	высшей группы должностей гражданской службы – не менее шести лет стажа гражданской службы или не менее семи лет стажа работы по специальности, направлению подготовки; </li>
	<li>
	главной группы должностей гражданской службы – не менее четырех лет стажа гражданской службы или не менее пяти лет стажа работы по специальности, направлению подготовки; </li>
	<li>
	ведущей группы должностей гражданской службы – не менее двух лет стажа гражданской службы или не менее четырех лет стажа работы по специальности, направлению подготовки; </li>
	<li>
	старшей и младшей групп должностей гражданской службы – без предъявления требований к стажу. </li>
</ol>
